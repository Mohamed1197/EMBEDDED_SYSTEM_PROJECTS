#include "STD_TYPES.h"
#include "Delay.h"

void Delay(u32 Copy_u32Delay)
{
	for(u32 i =0  ; i < Copy_u32Delay ; i++)
	{
		for(u32 j =0  ; j < 8050 ; j++)
		{
			asm("NOP");
		}
	}
}
